    sys.stdout.flush()
    time.sleep(0.5)
    print('\r\OuO ', end="")
    sys.stdout.flush()
    time.sleep(0.5)
    print('\r OuO/', end="")
    sys.stdout.flush()
    time.sleep(0.5)
    print('\r\OuO/', end="")
    sys.stdout.flush()
    time.sleep(0.5)
    print('\rdOuO ', end="")
    sys.stdout.flush()
    time.sleep(0.5)
    print('\r OuOb', end="")
    sys.stdout.flush()
